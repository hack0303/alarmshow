package com.creating.www.beans.root;

public class ConfigParamBean {
private int id;
private String key;
private String value;
private String description;
private String directionForUse;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getKey() {
	return key;
}
public void setKey(String key) {
	this.key = key;
}
public String getValue() {
	return value;
}
public void setValue(String value) {
	this.value = value;
}

public String getDescription() {
	return description;
}
public void setDescription(String description) {
	this.description = description;
}
public String getDirectionForUse() {
	return directionForUse;
}
public void setDirectionForUse(String directionForUse) {
	this.directionForUse = directionForUse;
}

}
